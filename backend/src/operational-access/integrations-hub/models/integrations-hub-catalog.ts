import { z } from "zod";

/** IH-001 — Integrations Hub category taxonomy (Version 1). */
export const INTEGRATIONS_HUB_CATEGORIES = [
  "marketplaces",
  "suppliers",
  "ecommerce",
  "payments",
  "advertising",
  "analytics",
  "communication",
  "ai_providers",
] as const;

export type IntegrationsHubCategory = (typeof INTEGRATIONS_HUB_CATEGORIES)[number];

export const INTEGRATIONS_HUB_DISPLAY_STATUSES = [
  "connected",
  "not_connected",
  "needs_reauth",
  "disabled",
  "error",
] as const;

export type IntegrationsHubDisplayStatus = (typeof INTEGRATIONS_HUB_DISPLAY_STATUSES)[number];

export const integrationsHubDefinitionSchema = z.object({
  integrationId: z.string().min(1),
  displayName: z.string().min(1),
  category: z.enum(INTEGRATIONS_HUB_CATEGORIES),
  purpose: z.string().min(1),
  whyEmpireNeedsIt: z.string().min(1),
  oneTimeSetup: z.literal(true),
  /** OAR platform id when tracked in empire-platform-catalog */
  platformId: z.string().optional(),
  /** marketplace-infrastructure-engine id for connect flow */
  marketplaceInfrastructureId: z.string().optional(),
  /** reality-integration provider id for vault connect */
  realityProviderId: z.string().optional(),
  connectKind: z.enum(["marketplace", "reality", "env", "future"]),
  documentationUrl: z.string().url().optional(),
  future: z.boolean().default(false),
});

export type IntegrationsHubDefinition = z.input<typeof integrationsHubDefinitionSchema>;

