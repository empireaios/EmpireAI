export function getIntegrationsHubDefinition(integrationId: string): IntegrationsHubDefinition | undefined {
  return INTEGRATIONS_HUB_CATALOG.find((d) => d.integrationId === integrationId);
}

export const integrationsHubItemSchema = z.object({
  integrationId: z.string(),
  displayName: z.string(),
  category: z.enum(INTEGRATIONS_HUB_CATEGORIES),
  purpose: z.string(),
  whyEmpireNeedsIt: z.string(),
  oneTimeSetup: z.literal(true),
  displayStatus: z.enum(INTEGRATIONS_HUB_DISPLAY_STATUSES),
  productionStatus: z.enum(["production", "sandbox", "architecture_only", "not_configured"]),
  lastVerification: z.string().datetime({ offset: true }).nullable(),
  connectKind: z.enum(["marketplace", "reality", "env", "future"]),
  canConnect: z.boolean(),
  canReconnect: z.boolean(),
  future: z.boolean(),
  documentationUrl: z.string().url().optional(),
  oarAccessState: z.string().optional(),
  restrictions: z.array(z.string()),
});

export type IntegrationsHubItem = z.infer<typeof integrationsHubItemSchema>;

export const integrationsHubDashboardSchema = z.object({
  moduleId: z.literal("integrations-hub"),
  missionId: z.literal("IH-001"),
  doctrineRef: z.literal("REAL-051A"),
  workspaceId: z.string(),
  categories: z.array(
    z.object({
      category: z.enum(INTEGRATIONS_HUB_CATEGORIES),
      label: z.string(),
      integrations: z.array(integrationsHubItemSchema),
    }),
  ),
  summary: z.object({
    total: z.number().int(),
    connected: z.number().int(),
    notConnected: z.number().int(),
    needsReauth: z.number().int(),
    disabled: z.number().int(),
    error: z.number().int(),
  }),
  computedAt: z.string().datetime({ offset: true }),
});

export type IntegrationsHubDashboard = z.infer<typeof integrationsHubDashboardSchema>;

